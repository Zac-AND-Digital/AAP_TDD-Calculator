package calculate;

public class ModelCalculate {

    long add(long x, long y) {
        return x + y;
    }

    long subtract(long x, long y) {
        return x - y;
    }

    double multiply(double x, double y) {
        return x * y;
    }

    double divide(double x, double y) {
        return x / y;
    }

}
